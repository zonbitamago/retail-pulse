// 辞書を検索用に平坦化（キーワード → タグ名）
const KEYWORD_LIST: { word: string; tag: string }[] = [];
for (const [, words] of Object.entries(DICTIONARY)) {
  for (const word of words) {
    KEYWORD_LIST.push({ word, tag: word });
  }
}
// 長いキーワードから先にマッチさせる（「セブン-イレブン」が「セブン」より先に）
KEYWORD_LIST.sort((a, b) => b.word.length - a.word.length);

export function extractKeywords(title: string): string[] {
  const tags = new Set<string>();
  const titleLower = title.toLowerCase();

  for (const { word, tag } of KEYWORD_LIST) {
    if (titleLower.includes(word.toLowerCase())) {
      tags.add(tag);
    }
  }

  return [...tags];
}
